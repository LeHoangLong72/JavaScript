/// toán tử logic
let i = 7;
// kiểm tra xem i > 0 và i < 10 không?
console.log(i > 0 && i < 10);
console.log(i > 0);
console.log(i < 10);

// kiểm tra xem i > 0 hoặc i < 10 không?
console.log(i > 0 || i < 10);
console.log(i > 0);
console.log(i < 10);

// toán tử đảo ngược !
console.log(!(i < 10));
